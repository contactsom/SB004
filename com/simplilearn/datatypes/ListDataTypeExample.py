print("This is example of Lists Data type in python")

print("****** START- LIST ********")

countryName=["INDIA","UK","USA","NEPAL","ISREAL","PAKISTAN"]
# countryName is the list of String type
print(type(countryName)) # list
print(countryName) #['INDIA', 'UK', 'USA', 'NEPAL', 'ISREAL', 'PAKISTAN']
print("*_______________________*")

employeeSalery=[1000,400,600,290]
# employeeSalery is the list of int type
print(type(employeeSalery)) #list
print(employeeSalery) #[1000,400,600,290]


print("*_______________________*")

todayTemp=[10.6,4.5,6.9,2.9]
# todayTemp is the list of float type
print(type(todayTemp)) #list
print(todayTemp) #[10.6,4.5,6.9,2.9]

print("****** END- LIST ********")
print("*_______________________*")