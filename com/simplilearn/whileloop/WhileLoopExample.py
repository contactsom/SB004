# Iteration 2 = true , print "I am While" , i = 1+1 = 2
# Iteration 3 = true , print "I am While" , i = 2+1 = 3
# Iteration 4 = true , print "I am While" , i = 3+1 = 4
# Iteration 5 = true , print "I am While" , i = 4+1 = 5
# Iteration 5 = false , loop terminated :)

