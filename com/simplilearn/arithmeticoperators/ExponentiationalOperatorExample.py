print("This is example for Exponentiation Operator in python")

print("****** START- Exponentiation OPERATOR ********")
a = 2
b = 2
c = a**b
# a to the power b means : a * a * a ..... b times
# Here b= 2 , Hence a * a i.e 2 * 2 = 4
print(c) # 4


x=2
y=3
z= x**y
# x to the power y means : x * x * x ..... y times
# Here y = 3 , Hence x * x * x i.e 2 * 2 * 2 = 8
print(z) # 8
print(type(z))
